package com.example.travelplus;

public class CarLocation {
    public double fromLat;
    public double fromLon;
    public double toLat;
    public double toLon;

    public CarLocation(double fromLat, double fromLon, double toLat, double toLon) {
        this.fromLat = fromLat;
        this.fromLon = fromLon;
        this.toLat = toLat;
        this.toLon = toLon;
    }
}
